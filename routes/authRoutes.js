const express = require('express');
const {
  registerSchema,
  loginSchema,
  forgotPasswordSchema,
  resetPasswordSchema,
  setNewPasswordSchema,
} = require('../validators/authValidators');
const { registerUser, loginUser, forgotPassword, resetPassword, setNewPassword } = require('../controllers/authController');
const { validateRequestBody } = require('../middleware/joiValidation');

const router = express.Router();

router.post('/register', validateRequestBody(registerSchema), registerUser);
router.post('/login', validateRequestBody(loginSchema), loginUser);
router.post('/forgot-password', validateRequestBody(forgotPasswordSchema), forgotPassword);
router.post('/reset-password', validateRequestBody(resetPasswordSchema), resetPassword);
router.post('/set-new-password/:token', validateRequestBody(setNewPasswordSchema), setNewPassword )

module.exports = router;

